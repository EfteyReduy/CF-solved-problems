        long long j, one=0;
        for( j=0; j<=k and j<n; j++)
        {
            if(s[j]=='1')
                one++;
        }
        long long ans=0;
        ans+=(one*one);

        long long  sub=one;
        j=1;
        //cout<<n-k<<endl;
        while(j<n-k)
        {

            sub=one;
            if(s[j-1]=='1')
                sub--;
            if(s[j-1]=='1')
                one--;
            if(s[j+k]=='1')
                one++;

            ans+=(one*one);
            ans-=(sub*sub);
            j++;
        }

//        if(n-1>k)
