#include<bits/stdc++.h>

using namespace std;


bool status[200000];
int dif[100000]={0};

void siv(int N) {
    int sq = sqrt(N);

    for (int i = 4; i <= N; i += 2) {
        status[i] = 1;
    }

    for (int i = 3; i <= sq; i += 2) {
        if (status[i] == 0) {
            for (int j = i * i; j <= N; j += i)
                status[j] = 1;
        }
    }

    status[1] = 1;
}

void setDif(){
    int j=0;
    for(int i=2; i<400000 ; i++)
    {
        if(status[i]==0)
        {
            dif[j]=(i);
            j++;
        }
    }
}


int main()
{
    siv(100000);
    int n=0;

    for(int i=0; i<100000; i++)
    {
        if(status[i]==0)
            n++;
    }
    cout<<n;
}
